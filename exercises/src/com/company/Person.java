package com.company;

public class Person {
    String name;
    int age;

    //constructor
    public Person(String C_name, int C_age ){
        name = C_name;
        age = C_age;
    }

    //toString method
    public String toString() {
        return "Main{" +
                "name='" + name + '\'' +
                ", age=" + age +
                '}';
    }





}
